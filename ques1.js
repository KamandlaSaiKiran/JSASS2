/* The ternary operator is the short hand notation of if-else and if-else-if cases 
    Synatx: Operation?expression1:expression2    
     here is operation is true then it will execute the expression1 if not it will execute the expression2
     below the code is an example for execution of ternary operators which checks whether the number is even or odd
*/
